                    </div>
                    <p>На экране появляется подсказка. На ней знаком + обозначено то, что распечатал ваш код. Знаком - – то, что ожидалось. Сравнивая эти два значения, можно увидеть ошибку. В примере выше видно, что в выводе пропущена буква H.</p>
