                        <div class="container"></div>
                        <div class="container1">
                            <p>|  Error:</p>
                            <p>|  unclosed string literal</p>
                            <p>|  System.out.println("alala</p>
                        </div>
                        <p>С одной стороны, ошибки синтаксиса — самые простые, потому что они связаны исключительно с грамматическими правилами написания кода, а не с самим смыслом кода. Их легко исправить — нужно лишь найти нарушение в записи.</p>
