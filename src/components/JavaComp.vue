                        <p>В человеческих языках грамматика важна, но текст с ошибками чаще всего можно понять и прочитать. В программировании все строго. Любое мельчайшее нарушение, и программа не запустится.</p>
                        <p>Примером может быть забытая ;, неправильно расставленные скобки и другие детали. Подобные ошибки называются синтаксическими, потому что они нарушают правила синтаксиса языка.</p>
                        <p>Если программа на Java написана синтаксически некорректно, то компилятор выводит на экран:</p>
                        <li class="hal">Сообщение об ошибке</li>
                        <li class="hal">Указание на файл</li>
                        <li class="hal">Строчка в файле, где по его мнению произошла ошибка</li>
                        <p>Ниже пример кода с синтаксической ошибкой:</p>
                        <div class="container2">
                        <p>System.out.println("alala</p>
                        </div>
