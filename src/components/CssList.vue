                    <h3>CSS: Приоритет стилей</h3>
                    <p>Говоря о каскадности, возникает вопрос: «А что произойдёт, если в разных источниках будут одинаковые правила для элемента, но с разными значениями?». В таком случае есть правила, определяющие приоритеты стилей из разных источников. По степени важности список выглядит следующим образом:</p>
                    <ul>
                        <li class="hal">Стили в атрибуте тега</li>
                        <li class="hal">Стили в отдельном файле</li>
                        <li class="hal">Стили по умолчанию, которые добавляет браузер</li>
                    </ul>
                    <p>Значения, указанные в атрибуте style будут важнее свойств в теге &lt;/style&gt;, а они будут важнее стандартных стилей браузера.</p>
                    <h3>Приоритеты селекторов</h3>
                    <p>Важной чертой CSS является то, что разные типы селекторов имеют разный приоритет. Если стили противоречат друг другу в разных селекторах, то вступает в дело принцип приоритета.</p>
                    <p>Разберём на примере. Создадим параграф с классом red и идентификатором blue</p>
                    <div class="container2">
                        <p>&lt;p id="blue" class="red"&gt;Текст с классом red и идентификатором blue&lt;/p&gt;</p>
                    </div>
                    <p>Добавим противоречащие друг другу стили для класса, идентификатора и тега:</p>
                    <div class="container2">
                        <p>p {</p>
                        <p>  color: black;</p>
                        <p>}</p>
                        <p>.red {</p>
                        <p>  color: red;</p>
                        <p>}</p>
                        <p>#blue {</p>
                        <p>  color: blue;</p>
                        <p>}</p>
                    </div>
                    <p>Какого цвета будет параграф? Попробуйте выполнить этот пример в редакторе. Вы увидите, что текст будет синего цвета. С чем это связано?</p>
                    <p>У селекторов также существуют приоритеты. Условно можно расставить селекторы в следующем порядке по приоритету:</p>
                    <ul>
                        <ol class="hal">Селектор по идентификатору (#blue)</ol>
                        <ol class="hal">Селектор по классу (.red)</ol>
                        <ol class="hal">Селектор по тегу (p)</ol>
                    </ul>
                    <p>Где 1 — самый высокий приоритет, а 3 — самый низкий.</p>
                    <p>По этим правилам можно понять, что свойства указанные в селекторе по идентификатору будет обладать большим приоритетом, чем стили у селектора по тегу и классу. Поэтому параграф в прошлом примере был синим.</p>
                </div>
