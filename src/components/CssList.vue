                    <h3>CSS: Каскадность</h3>
                    <p>Мы уже знаем, что CSS переводится как «Каскадные таблицы стилей». С таблицей стилей к этому моменту всё ясно, но что значит каскадные? Дело в том, что указать стили для элемента можно не только в одном месте.</p>
                    <p>Представьте каскад водопадов. Они перетекают друг в друга, накапливая всё больше и больше воды. В конечном итоге весь каскад окажется в общем бассейне. В CSS слово каскадность обозначает такое же действие, только вместо водопадов перетекают CSS свойства. Другими словами каскадность — возможность языка CSS накладывать свойства друг на друга, а также расширять свойства в селекторах.</p>   
                    <p>Создадим блок с классами text-bold и text-dark и укажем несколько свойств в разных селекторах:</p>
                    <div class="container2">
                        <p>&lt;p class="text-bold text-dark"&gt;Важный текст о вреде сложного CSS&lt;/p&gt;</p>
                    </div>
                    <p></p>
                    <div class="container2">
                        <p>p {</p>
                        <p>  font-size: 20px;</p>
                        <p>}</p>
                        <p>.text-bold {</p>
                        <p>  font-weight: bold;</p>
                        <p>}</p>
                        <p>.text-dark {</p>
                        <p>  color: #333;</p>
                        <p>}</p>
                    </div>
                    <p>Если вставить этот пример в редактор, то выведется жирный текст размером 20 пикселей тёмного цвета. Произошло это благодаря каскадности — браузер объединил все стили из разных селекторов и получил следующие инструкции для стилизации элемента:</p>
                    <div class="container2">
                        <p>p {</p>
                        <p>  font-size: 20px;</p>
                        <p>  font-weight: bold;</p>
                        <p>  color: #333;</p>
                        <p>}</p>
                    </div>
                    <p>Это и есть каскадность стилей в CSS. Хоть мы и использовали разные селекторы, но, в конечном итоге, браузер объединил их в единый набор правил для элемента</p>
                </div>
            </div>




