                        <h3>Python: Синтаксические ошибки</h3>
                        <p>Если программа на Python написана синтаксически некорректно, то интерпретатор выводит на экран соответствующее сообщение. Также он указывает на файл и строчку, где произошла ошибка.</p>
                        <p><b>Синтаксическая ошибка</b> возникает в том случае, когда код записали с нарушением грамматических правил. В естественных языках грамматика важна, но текст с ошибками обычно можно понять и прочитать. В программировании все строго. Мельчайшее нарушение — и программа даже не запустится. Примером может быть забытая ;, неправильно расставленные скобки и другие детали.</p>
                        <p>Вот пример кода с синтаксической ошибкой:</p>
                        <div class="container2">
