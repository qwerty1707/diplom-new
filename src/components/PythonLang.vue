                        <div class="container2">
                        <p>$ python index.py</p>
                        <p>File "index.py", line 1</p>
                        <p>print('Hodor)</p>
                        <p>            ^</p>
                        <p>SyntaxError: EOL while scanning string literal</p>
                        </div>
                        <p>С одной стороны, ошибки синтаксиса — самые простые, потому что они связаны с грамматическими правилами написания кода, а не со смыслом кода. Их легко исправить: нужно лишь найти нарушение в записи. С другой стороны, интерпретатор не всегда может четко указать на это нарушение. Поэтому бывает, что забытую скобку нужно поставить не туда, куда указывает сообщение об ошибке.</p>
                       
