                            <li class="hal">Второй элемент списка</li>
                        </ul>
                    </div>
                    <p>Для определения списка используется тег &lt;ul&gt;, который указывает, что это маркированный список. Внутри располагаются теги &lt;li&gt;, определяющие элементы нашего списка.</p>
                    <p>Такие вложенности похожи на деревья, где есть вершина дерева (тег &lt;ul&gt;) и листья (теги &lt;li&gt;)</p>
