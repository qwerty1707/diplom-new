                    <h3>HTML: Вложенность тегов</h3>
                    <p>Важной концепцией HTML является вложенность тегов. Внутрь HTML-тега можно помещать не только текст, как было ранее, но и другие теги. Это позволяет создавать сложную вёрстку, в которой области отделены друг от друга: меню от шапки сайта, рекламный блок от основного контента и так далее.</p>
                    <p>Некоторые элементы в HTML требуют вложенности одних тегов в другие. Например, списки:</p>
                    <div class="container2">
